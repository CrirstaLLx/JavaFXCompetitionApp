/**
 * The CompetitionStage enum represents the stages of the competition in the Slovakia Got Talent application.
 * It includes casting, semi-final, and final stages.
 */
package com.example.skgottalent.competition;

public enum CompetitionStage {
    /** The casting stage of the competition. */
    CASTING,

    /** The semi-final stage of the competition. */
    SEMI_FINAL,

    /** The final stage of the competition. */
    FINAL
}
